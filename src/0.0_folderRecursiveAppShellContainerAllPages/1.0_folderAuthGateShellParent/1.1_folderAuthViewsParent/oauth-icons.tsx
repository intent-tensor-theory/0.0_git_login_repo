type OAuthProvider = 'google' | 'github' | 'apple' | 'microsoft' | 'twitter' | 'discord' | 'linkedin';

const ICON_MAP: Record<OAuthProvider, React.FC<IconProps>> = {
  google: GoogleIcon,
  github: GitHubIcon,
  apple: AppleIcon,
  microsoft: MicrosoftIcon,
  twitter: TwitterIcon,
  discord: DiscordIcon,
  linkedin: LinkedInIcon,
};

/**
 * Get the icon component for a provider
 */
export function getOAuthIcon(provider: OAuthProvider): React.FC<IconProps> {
  return ICON_MAP[provider];
}

/**
 * Render an OAuth icon by provider name
 */
export const OAuthIcon: React.FC<{ provider: OAuthProvider } & IconProps> = ({ 
  provider, 
  ...props 
}) => {
  const IconComponent = ICON_MAP[provider];
  return IconComponent ? <IconComponent {...props} /> : null;
};

export default OAuthIcon;
